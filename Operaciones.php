/**
 * Description of Operaciones
 *
 * @author faranzabe
 */
class Operaciones {

    /**
     * @param int $a
     * @param int $b
     * @return int
     * */
    function suma($a, $b) {
        return $a + $b;
    }

    /**
     * @param int $a
     * @param int $b
     * @return int
     * */
    function resta($a, $b) {
        return $a - $b;
    }

}
